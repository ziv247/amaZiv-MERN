import jwt from "jsonwebtoken";
import { User } from "./models/userModel";
export const generateToken = (user: User) => {
  return jwt.sign(
    {
      _id: user._id,
      name: user.name,
      email: user.email,
      isAdmin: user.isAdmin,
    },
    process.env.JWT_SECRET || "nunagidshezeloimza",
    { expiresIn: "30d" }
  );
};
